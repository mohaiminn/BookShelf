        public override string[] GetRolesForUser(string username)
        {
            using (var context= new EbookWebEntities())
            {
                var result = (from Users in context.Users
                              join Roles in context.Roles on Users.Id equals Roles.UserID
                              where Users.Username == username
                              select Roles.Role).ToArray();

                return result;
            }
            
